#include "libft.h"

int	ft_strlead(const char *s1, const char *s2)
{
	size_t	it;

	it = 0;
	while (s1[it] && (s1[it] == s2[it] && s2[it]))
		it++;
	if (s2[it] == '\0')
		return (1);
	return (0);
}
